//Basics Comparision
console.log(2 > 1 ); // true
console.log(2 >= 1 ); // true
console.log(2 < 1 ); // false
console.log(2 <= 1 ); // false
console.log(2 != 1 ); // true

console.log("2" > 1 ); // true
console.log("02" > 1); // true

console.log(null > 0); // false
console.log(null == 0); // false
console.log(null >= 0); // true  // it is true because < > , <= ,>=  is convert null to 0 .

console.log(undefined > 0);
console.log(undefined == 0);
console.log(undefined >= 0);


